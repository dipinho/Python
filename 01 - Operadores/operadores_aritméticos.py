print("Operadores airméticos")

num1 = 16
num2 = 5

#Adição
print("Resultado da soma é ",num1 + num2)  #só pode concatenar str com int usando virgula, e não + como no java

#Subtração
print("Resultado da subtração é ",num1 - num2) 

#Multiplicação
print("Resultado da multiplicação é ",num1 * num2) 

#Divisão
print("Resultado da divisão é ",num1 / num2) 

#Divisão Inteira
print("Resultado da divisão é ",num1 // num2) 

#Módulo 
print("Resultado do módulo é ",num1 % num2) # Resto da divisão

#Exponenciação
print("Resultado da divisão é ",num1 ** num2) 